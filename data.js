// ============================================
// Storage Manager (Supabase integration)
// ============================================
const StorageManager = {
  async init() {
    // No local initialization needed, data resides in Supabase.
    console.log('StorageManager initialized with Supabase');
  },

  async getAll() {
    const { data, error } = await supabaseClient
      .from('inventario')
      .select('*')
      .order('modulo', { ascending: true });
    
    if (error) {
      console.error("Error fetching data from Supabase:", error);
      return [];
    }
    return data.map(item => ({
      id: item.id,
      codigoSAP: item.codigo_sap,
      cantidad: item.cantidad,
      descripcion: item.descripcion || '',
      proveedor: item.proveedor || 'N/A',
      modulo: item.modulo,
      prealistamiento: item.prealistamiento || ''
    }));
  },

  async getByModule(modulo) {
    const { data, error } = await supabaseClient
      .from('inventario')
      .select('*')
      .eq('modulo', modulo);
    
    if (error) {
      console.error(`Error fetching modulo ${modulo} from Supabase:`, error);
      return [];
    }
    return data.map(item => ({
      id: item.id,
      codigoSAP: item.codigo_sap,
      cantidad: item.cantidad,
      descripcion: item.descripcion || '',
      proveedor: item.proveedor || 'N/A',
      modulo: item.modulo,
      prealistamiento: item.prealistamiento || ''
    }));
  },

  async getById(id) {
    const { data, error } = await supabaseClient
      .from('inventario')
      .select('*')
      .eq('id', id)
      .single();
    
    if (error) {
      console.error(`Error fetching item ${id} from Supabase:`, error);
      return null;
    }
    return {
      id: data.id,
      codigoSAP: data.codigo_sap,
      cantidad: data.cantidad,
      descripcion: data.descripcion || '',
      proveedor: data.proveedor || 'N/A',
      modulo: data.modulo,
      prealistamiento: data.prealistamiento || ''
    };
  },

  async update(id, updates) {
    const dbUpdates = {};
    if (updates.codigoSAP !== undefined) dbUpdates.codigo_sap = updates.codigoSAP;
    if (updates.cantidad !== undefined) dbUpdates.cantidad = updates.cantidad;
    if (updates.descripcion !== undefined) dbUpdates.descripcion = updates.descripcion;
    if (updates.proveedor !== undefined) dbUpdates.proveedor = updates.proveedor;
    if (updates.modulo !== undefined) dbUpdates.modulo = updates.modulo;
    if (updates.prealistamiento !== undefined) dbUpdates.prealistamiento = updates.prealistamiento;
    dbUpdates.updated_at = new Date().toISOString();

    const { data, error } = await supabaseClient
      .from('inventario')
      .update(dbUpdates)
      .eq('id', id)
      .select()
      .single();

    if (error) {
      console.error(`Error updating item ${id} in Supabase:`, error);
      return null;
    }
    return {
      id: data.id,
      codigoSAP: data.codigo_sap,
      cantidad: data.cantidad,
      descripcion: data.descripcion || '',
      proveedor: data.proveedor || 'N/A',
      modulo: data.modulo,
      prealistamiento: data.prealistamiento || ''
    };
  },

  async getGrouped() {
    const all = await this.getAll();
    const grouped = {};
    all.forEach(item => {
      if (!grouped[item.modulo]) grouped[item.modulo] = [];
      grouped[item.modulo].push(item);
    });
    return grouped;
  },

  async getStats() {
    const all = await this.getAll();
    const modules = new Set(all.map(d => d.modulo));
    const totalCantidad = all.reduce((sum, d) => sum + (d.cantidad || 0), 0);
    return {
      totalItems: all.length,
      totalModules: modules.size,
      totalCantidad,
      proveedores: [...new Set(all.map(d => d.proveedor).filter(p => p && p !== 'N/A'))]
    };
  }
};
